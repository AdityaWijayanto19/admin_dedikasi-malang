 <script>
        document.addEventListener("DOMContentLoaded", function() {
            // Kelas untuk link aktif
            const activeClasses = ['bg-primary-yellow', 'text-gray-800', 'font-medium'];
            // Kelas dasar (untuk memastikan hover tidak bentrok)
            const baseClasses = ['text-gray-300', 'hover:bg-gray-700', 'hover:text-white'];

            const currentPath = window.location.pathname;
            const navLinks = document.querySelectorAll('#sidebar-nav a');

            navLinks.forEach(link => {
                const linkFolder = link.getAttribute('href').split('/')[1];

                if (linkFolder && currentPath.includes(`/${linkFolder}/`)) {
                    // Hapus kelas dasar terlebih dahulu
                    link.classList.remove(...baseClasses);
                    // Tambahkan kelas aktif
                    link.classList.add(...activeClasses);
                }
            });
        });
    </script>